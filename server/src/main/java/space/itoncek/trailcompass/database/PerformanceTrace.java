import jakarta.persistence.*;
import lombok.Getter;
import lombok.Setter;

import java.util.Set;

@Getter
@Setter
@Entity
public class PerformanceTrace {
	@Id
	String requestClassName;

	@ElementCollection
	@CollectionTable(name = "requestDuration", joinColumns = @JoinColumn(name = "requestClassName"))
	@Column(name = "requestDuration")
	Set<Float> requestDuration;
}
